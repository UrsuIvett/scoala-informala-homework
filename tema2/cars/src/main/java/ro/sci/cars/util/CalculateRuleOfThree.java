/*
  the following method will calculate with the rule of three, using 3 parameters
 */
public class CalculateRuleOfThree {

    public CalculateRuleOfThree() {
        double aa;
        double bb;
        double cc;
    }

    public  double calculate(double aa, double bb, double cc){
            return (aa*bb)/cc;
        }
    }

